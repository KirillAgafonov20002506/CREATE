# Запрашиваем у пользователя два числа
a = float(input("Введите первое число (делимое): "))
b = float(input("Введите второе число (делитель): "))

# Проверяем, не равен ли делитель нулю
if b == 0:
    print("Ошибка: деление на ноль невозможно!")
else:
    # Вычисляем остаток от деления
    remainder = a % b
    print(f"Остаток от деления {a} на {b} равен {remainder}")